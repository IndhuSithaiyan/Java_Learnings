package com.java.fundamentals;

public class PrimitiveDataTypes {

    public static void main(String[] args) {
//        Primitive Data Types
        byte theByte = -124;
        short theShort = 4_315;
        int theInt = 898_476_895;
        long theLong = 91_632_670_236L;
//        to read these number is difficult

        float theFloat = 23.421f;
        double theDouble = 3285_790.23023;

        boolean isAlive = true;
        char nameInitial = 'G';

        System.out.println(theByte);
        System.out.println(theShort);
        System.out.println(theInt);
        System.out.println(theLong);
        System.out.println(theFloat);
        System.out.println(theDouble);
        System.out.println(isAlive);
        System.out.println(nameInitial);

    }

}
